package pack02.variabletype;

public class Test01_Escape {
	public static void main(String[] args) {
		System.out.println("\\         /\\");
		System.out.println(" )        ( ')");
		System.out.println(" (        /)");
		System.out.println(" \\     (__)|");
	}

	// 정수형 변수 타입은 short / int / long 있다.
	
	// 문자 데이터를 담기 위한 데이터 타입은 char 이다.
	
	// 문자열 데이터를 담기 위한 데이터 타입은 String 이다.
	
	// long 타입은 데이터 뒤에 L 를 붙여햐 한다.
	
	// 문자열 내부에 표현 할 수 없는 문자나 tab 들여쓰기 등의 기능을 가진 것은 \t 이다.
}
